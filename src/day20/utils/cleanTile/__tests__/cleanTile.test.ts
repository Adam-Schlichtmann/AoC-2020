import cleanTile from "../cleanTile";
import test from "ava";
import { Tile } from "../../../Types";

test("cleanTile tests", async (t) => {
  t.deepEqual(
    cleanTile({
      id: 1,
      sides: [],
      neighbors: [],
      tile: ["#.#.", "#...", "###.", ".##."],
    }),
    ["..", "##"]
  );
  t.deepEqual(
    cleanTile({
      id: 1,
      sides: [],
      neighbors: [],
      tile: [
        ".##.#.###",
        ".#..#.###",
        ".#..#.#.#",
        "##..#.###",
        ".###....#",
        "##..#.###",
        ".#..#.##.",
        ".#..#.##.",
        "..#.#..#.",
      ],
    }),
    [
      "#..#.##",
      "#..#.#.",
      "#..#.##",
      "###....",
      "#..#.##",
      "#..#.##",
      "#..#.##",
    ]
  );
  t.deepEqual(
    cleanTile({
      id: 1,
      sides: [],
      neighbors: [],
      tile: [
        ".#..#..##.##.#.###",
        ".##.#.###.#..#..##",
        ".##.#.#.#.#..#.###",
        ".##.#.#.####.#.###",
        ".#....#.##.#.#...#",
        ".#.##.#.#.#...#.##",
        "#...#...#.#.###.##",
        ".#..#.####...#..##",
        ".###....#...#....#",
        ".###....#.##.....#",
        "##..#.#.#....#..##",
        "##..#.#####.##.###",
        ".#..#..#..#..#.##.",
        ".#..#.##..#..#..#.",
        ".#..#.##.....#.###",
        ".#..#.##..##.#.##.",
        "..#.#..#...#.#..#.",
        "..#.#..#...#.#..#.",
      ],
    }),
    [
      "##.#.###.#..#..#",
      "##.#.#.#.#..#.##",
      "##.#.#.####.#.##",
      "#....#.##.#.#...",
      "#.##.#.#.#...#.#",
      "...#...#.#.###.#",
      "#..#.####...#..#",
      "###....#...#....",
      "###....#.##.....",
      "#..#.#.#....#..#",
      "#..#.#####.##.##",
      "#..#..#..#..#.##",
      "#..#.##..#..#..#",
      "#..#.##.....#.##",
      "#..#.##..##.#.##",
      ".#.#..#...#.#..#",
    ]
  );
});
