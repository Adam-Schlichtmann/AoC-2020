import { t2729, t2971, tiles } from "../../tile.testdata";
import getNextRowStart from "../getNextRowStart";
import test from "ava";

test("getNextRowStart tests", async (t) => {
  t.deepEqual(
    getNextRowStart(
      [
        [
          "#...##.#....###..###",
          "..#.#..#.####...#.#.",
          ".###....#...#....#..",
          "###.##.##..#.#.#..##",
          ".###.#######...#.###",
          ".##.#....###.##.###.",
          "#...##########.#...#",
          ".....#..###...##..#.",
          "#.####...###..#.....",
          "#.##...##...##.#..#.",
        ],
      ],
      tiles.filter((t) => ![1951, 2311].includes(t.id))
    ),
    t2729
  );

  t.deepEqual(
    getNextRowStart(
      [
        [
          "#...##.#....###..###",
          "..#.#..#.####...#.#.",
          ".###....#...#....#..",
          "###.##.##..#.#.#..##",
          ".###.#######...#.###",
          ".##.#....###.##.###.",
          "#...##########.#...#",
          ".....#..###...##..#.",
          "#.####...###..#.....",
          "#.##...##...##.#..#.",
        ],
        [
          "#.##...##.",
          "##..#.##..",
          "##.####...",
          "####.#.#..",
          ".#.####...",
          ".##..##.#.",
          "....#..#.#",
          "..#.#.....",
          "####.#....",
          "...#.#.#.#",
        ],
      ],
      tiles.filter((t) => ![1951, 2311, 2729].includes(t.id))
    ),
    t2971
  );
});
